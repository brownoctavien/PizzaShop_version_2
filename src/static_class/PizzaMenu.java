package static_class;
public class PizzaMenu {

    // creating variables
    String topping;
    String size;


    // default constructor
    public PizzaMenu()
    {

    }

    // setters and getters
    public void setTopping(String topping)
    {
        this.topping = topping;
    }

    public String getTopping()
    {
        return topping;
    }

    public void setSize(String size)
    {
        this.size = size;
    }

    public String getSize()
    {
       return size;
    }



} // end of PizzaMenu class
